#include <stdio.h>
#include "../fmt.h"
#include "../str.h"

struct fmt_test {
  unsigned int sig;
  unsigned int len;
  float num;
  char *str;
};

static const struct fmt_test tests[] = {
  /* zero tests */
  { 0, 0, 0.0, "" },
  { 1, 3, 0.0, "0.0" },
  { 2, 3, 0.0, "0.0" },
  { 3, 3, 0.0, "0.0" },
  { 4, 3, 0.0, "0.0" },
  { 5, 3, 0.0, "0.0" },
  { 6, 3, 0.0, "0.0" },

  /* negative zero tests */
  { 0, 0, -0.0, "" },
  { 1, 4, -0.0, "-0.0" },
  { 2, 4, -0.0, "-0.0" },
  { 3, 4, -0.0, "-0.0" },
  { 4, 4, -0.0, "-0.0" },
  { 5, 4, -0.0, "-0.0" },
  { 6, 4, -0.0, "-0.0" },

  /* integral */
  { 0, 0, 123456.0, "" },
  { 1, 8, 123456.0, "123456.0" },
  { 2, 8, 123456.0, "123456.0" },
  { 3, 8, 123456.0, "123456.0" },
  { 4, 8, 123456.0, "123456.0" },
  { 5, 8, 123456.0, "123456.0" },
  { 6, 8, 123456.0, "123456.0" },

  /* fractional */
  { 0, 0, 0.123456, "" },
  { 1, 3, 0.123456, "0.1" },
  { 2, 4, 0.123456, "0.12" },
  { 3, 5, 0.123456, "0.123" },
  { 4, 6, 0.123456, "0.1235" },
  { 5, 7, 0.123456, "0.12346" },
  { 6, 8, 0.123456, "0.123456" },

  /* negative integral */
  { 0, 0, -123456.0, "" },
  { 1, 9, -123456.0, "-123456.0" },
  { 2, 9, -123456.0, "-123456.0" },
  { 3, 9, -123456.0, "-123456.0" },
  { 4, 9, -123456.0, "-123456.0" },
  { 5, 9, -123456.0, "-123456.0" },
  { 6, 9, -123456.0, "-123456.0" },

  /* negative fractional */
  { 0, 0, -0.123456, "" },
  { 1, 4, -0.123456, "-0.1" },
  { 2, 5, -0.123456, "-0.12" },
  { 3, 6, -0.123456, "-0.123" },
  { 4, 7, -0.123456, "-0.1235" },
  { 5, 8, -0.123456, "-0.12346" },
  { 6, 9, -0.123456, "-0.123456" },

  /* large */
  { 0, 0, 1234567.9876543, "" },
  { 1, 5, 1234567.9876543, "1.2e6" },
  { 2, 6, 1234567.9876543, "1.23e6" },
  { 3, 7, 1234567.9876543, "1.235e6" },
  { 4, 8, 1234567.9876543, "1.2346e6" },
  { 5, 9, 1234567.9876543, "1.23457e6" },
  { 6, 10, 1234567.9876543, "1.234568e6" },

  /* negative large */
  { 0, 0, -1234567.9876543, "" },
  { 1, 6, -1234567.9876543, "-1.2e6" },
  { 2, 7, -1234567.9876543, "-1.23e6" },
  { 3, 8, -1234567.9876543, "-1.235e6" },
  { 4, 9, -1234567.9876543, "-1.2346e6" },
  { 5, 10, -1234567.9876543, "-1.23457e6" },
  { 6, 11, -1234567.9876543, "-1.234568e6" },

  /* FLT_MAX, FLT_MIN */
  { 6, 11, 1e37, "9.999998e36" },
  { 6, 12, 1e-37, "9.999999e-38" },
};

int main(void)
{
  char cnum[FMT_FLOAT];
  unsigned int ind;
  unsigned int len;

  for (ind = 0; ind < sizeof(tests) / sizeof(struct fmt_test); ++ind) {
    len = fmt_float(cnum, tests[ind].num, tests[ind].sig);
    if (len != tests[ind].len) {
      printf("fail: [%u] len %u != %u %s\n", ind, len, tests[ind].len, tests[ind].str);
      return 1;
    }
    len = fmt_float(FMT_LEN, tests[ind].num, tests[ind].sig);
    if (len != tests[ind].len) {
      printf("fail: [%u] FMT_LEN len %u != %u\n", ind, len, tests[ind].len);
      return 1;
    }
    cnum[len] = 0;
    if (!str_same(cnum, tests[ind].str)) {
      printf("fail: [%u] %s != %s\n", ind, cnum, tests[ind].str);
      return 1;
    }
    printf("[%u] %s == %s\n", ind, tests[ind].str, cnum);
  }

  return 0;
}
